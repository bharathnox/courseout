const mongoose = require("mongoose");

const stdUserSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const stdUser = mongoose.model("StdUser", stdUserSchema);

module.exports = stdUser;